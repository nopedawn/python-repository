# List, menggunakan []
tipedata1 = ['piton', 400, False, 2.5]

# Tuple, menggunakan ()
tipedata2 = ('piton', 400, False, 2.5)

# Set, menggunakan {}
tipedata3 = {'piton', 400, False, 2.5}

# Dictionary, menggunakan {key:value}
tipedata4 = {"string":'piton', "integer":400, "boolean":False, "float":2.5}

# mengeprint variable  diatas menggunakan print()
print(tipedata1)
print(tipedata2)
print(tipedata3)
print(tipedata4)
